from pathlib import Path

DOCS = Path(__file__).resolve().parents[1] / "docs"


def write(name, body):
    p = DOCS / name
    p.write_text(body.strip() + "\n", encoding="utf-8")
    print("card", name, p.stat().st_size)


def card_doc(title, detail, oral, cards, path="./路径-Java后端.md"):
    lines = [
        f"# {title} · 卡片速记",
        "",
        "<!-- NAV:START -->",
        "> **只背要点。** 原理 → [详解](./" + detail + ") · 怎么说 → [面渣](./" + oral + ")",
        ">",
        f"> [首页](./README.md) · [如何使用](./如何使用本仓库.md) · [路径]({path})",
        ">",
        "<!-- NAV:END -->",
        "",
        "> 用法：遮住 **A**，只看 **Q** 回忆；卡壳回详解。",
        "",
        "---",
        "",
    ]
    for i, (q, a) in enumerate(cards, 1):
        lines.append(f"## {i}. {q}")
        lines.append("")
        lines.append(f"**A:** {a}")
        lines.append("")
    lines.append("---")
    lines.append("")
    lines.append(f"详解：[{detail}](./{detail}) · 面渣：[{oral}](./{oral})")
    return "\n".join(lines)


